def sanitize_filename(filename: str) -> str:
    """清洗文件名，移除或替换敏感词"""
    # 移除文件扩展名
    name_without_ext = Path(filename).stem

    # 按顺序替换敏感词（先替换长词，再替换短词）
    for sensitive_word, replacement in SENSITIVE_WORDS_ORDERED:
        # 跳过单字符替换，避免误伤复合词
        if len(sensitive_word) == 1:
            continue
        name_without_ext = name_without_ext.replace(sensitive_word, replacement)

    # 返回清洗后的文件名
    return name_without_ext


def sanitize_pdf_content(content: str) -> str:
    """清洗 PDF 文件内容，移除或替换敏感词"""
    # 按顺序替换敏感词（先替换长词，再替换短词）
    for sensitive_word, replacement in SENSITIVE_WORDS_ORDERED:
        # 跳过单字符替换，避免误伤复合词
        if len(sensitive_word) == 1:
            continue
        content = content.replace(sensitive_word, replacement)

    # 移除过于直白的描述（简化版，只替换明确的词汇）
    content = re.sub(r"色情", "不适当内容", content)
    content = re.sub(r"暴力", "冲突", content)

    # 返回清洗后的内容
    return content


