def should_skip_file(filename: str) -> tuple[bool, str | None]:
    """
    检查文件名是否包含高风险敏感词
    如果包含，返回 True（建议跳过）
    """
    filename_lower = filename.lower()
    for risk_word in HIGH_RISK_WORDS:
        if risk_word in filename_lower:
            return True, risk_word
    return False, None


